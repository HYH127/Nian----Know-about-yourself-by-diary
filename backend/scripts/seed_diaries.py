async def main():
    async with httpx.AsyncClient(timeout=30) as client:
        for i, diary in enumerate(DIARIES):
            print(f"[{i+1}/20] 导入 {diary['date']} — {diary['content'][:30]}...", end=" ")
            try:
                resp = await client.post(
                    f"{API_BASE}/diary",
                    json={"date": diary["date"], "content": diary["content"]},
                )
                resp.raise_for_status()
                print(f"OK (id={resp.json().get('id', '?')[:8]})")
            except Exception as e:
                print(f"FAILED: {e}")
                sys.exit(1)

            if i < len(DIARIES) - 1:
                print("  等待异步后处理...")
                await asyncio.sleep(3)

    print("\n全部 20 篇日记导入完成！")

if __name__ == "__main__":
    asyncio.run(main())