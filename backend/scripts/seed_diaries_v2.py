async def main():
    async with httpx.AsyncClient(timeout=30) as client:
        for i, diary in enumerate(DIARIES):
            preview = diary["content"][:40].replace("\n", " ")
            print(f"[{i+1}/10] 导入 {diary['date']} — {preview}...", end=" ")
            try:
                resp = await client.post(
                    f"{API_BASE}/diary",
                    json={"date": diary["date"], "content": diary["content"]},
                )
                resp.raise_for_status()
                data = resp.json()
                print(f"OK (id={data.get('id', '?')[:8]})")
            except Exception as e:
                print(f"FAILED: {e}")
                sys.exit(1)

            if i < len(DIARIES) - 1:
                print("  等待异步后处理（关系提取 + 画像构建）...")
                await asyncio.sleep(3)

    print("\n全部 10 篇日记导入完成！")
    print("现在去画像页查看 ProfileFragment，展开后应出现「查看日记 →」跳转链接。")


if __name__ == "__main__":
    asyncio.run(main())